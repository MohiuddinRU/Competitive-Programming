using namespace std;

int n,k, ara[110];
int pr[110];

int main() {

//    freopen("i.txt", "r", stdin);
//    freopen("o.txt", "w",stdout);

    int T;
    scanf("%d", &T);
    for(int t=1; t<=T; t++) {
        scanf("%d %d", &n,&k);
        for(int i=1; i<=k; i++) scanf("%d", &ara[i]);

        for(int i=0; i<=k; i++) pr[i] = MAX;
        pr[0] = 0;

        for(int i=1; i<=k; i++) if(ara[i] != -1) for(int w=i; w<=k; w++) pr[w] = min(pr[w], ara[i]+pr[w-i]);

        if(pr[k] == MAX) printf("-1\n");
        else printf("%d\n", pr[k]);
    }

    return 0;
}
