using namespace std;

struct info{
    int x,y,z;
    info() {x = y = z = 0;}
    info(int xx, int yy = 0, int zz = 0) {
        x = xx; y = yy; z = zz;
    }
};
bool cmpx(info x, info y) {
    return x.x < y.x;
}
bool cmpy(info x, info y) {
    return x.y < y.y;
}

int n,u,v;
vii graph[LIM+10];
int vis[LIM+10];

info dfs(int u) {
    vis[u] = 1;
    int sz = graph[u].size();
    int ans = 1, vol=0;
    for(int i=0; i<sz; i++) {
        int v = graph[u][i];
        if(vis[v]) continue;

        ans++;
        info tmp = dfs(v);
        ans += tmp.x;
        vol += tmp.y;
    }
    return info(ans, ans+vol);
}

